function normalizeLocale(locale) {
  const candidate = String(locale || "").trim().toLowerCase();
  return SUPPORTED_LOCALES.includes(candidate) ? candidate : DEFAULT_LOCALE;
}

function getSiteLanguage() {
  return normalizeLocale(AppSettingQueries.get(LANGUAGE_SETTING_KEY, DEFAULT_LOCALE));
}

function interpolate(template, params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => String(params[key] == null ? "" : params[key]));
}

function createTranslator(locale) {
  const current = normalizeLocale(locale);
  return function t(key, params = {}) {
    const dict = translations[current] || translations[DEFAULT_LOCALE];
    const fallback = translations[DEFAULT_LOCALE] || {};
    const template = dict[key] || fallback[key] || key;
    return interpolate(template, params);
  };
}

function getRuntimeTextMap(locale) {
  return runtimeTextMaps[normalizeLocale(locale)] || {};
}

